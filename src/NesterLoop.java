public class NesterLoop {
    public static void main(String[] args){
        //Nester loop = consist of outer loop and inner loop i, j and k, or x and y counters
        //each line the outer loop is executed again until in the false

        // extension print number 10 ...... 13
        for (int i=0; 1<=4; i++) {
            for (int j=0; j<=4; j++) {
                for (int k=0; k<=4; k++) {
                    System.out.println(i + " " + j + " " +k);
                }
            }
            }
        }
}
